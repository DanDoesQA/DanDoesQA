
public class SwitchWithEnums {

	public static void main(String[] args) {

		int month = 1;
		
		switch(month) {
		case 1:
			System.out.println("The month is January");
			break;
		case 2:
			System.out.println("The month is February");
			break;
		case 3:
			System.out.println("The month is March");
		}

	}

}
